	// Define matrix A
	mat_A := mat.NewDense(2, 2, []float64{
		float64(A.x), float64(B.x),
		float64(A.y), float64(B.y),
	})

	// Define vector b
	// b := mat.NewVecDense(2, []float64{float64(P.x), float64(P.y)})
